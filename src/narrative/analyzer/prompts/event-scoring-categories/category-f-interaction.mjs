/**
 * Stage 2：F类（互动/传播类）评分 Prompt
 * V1.0 - 3阶段架构的第二阶段
 */

export const CATEGORY_F_PROMPT_VERSION = 'V1.0';

export function buildCategoryFPrompt(eventDescription, eventClassification) {
  return `你是F类（互动/传播类）事件评分专家。

【事件描述】
主题：${eventDescription.eventTheme}
主体：${eventDescription.eventSubject}
是否超大IP：${eventDescription.isLargeIP}
事件内容：${eventDescription.eventContent}
时效性：${eventDescription.eventTiming}
关键实体：${eventDescription.keyEntities?.join(', ') || '无'}
性质标记：${eventClassification?.propertyMarkers ? JSON.stringify(eventClassification.propertyMarkers) : '无'}

╔══════════════════════════════════════════════════════════════════════════════╗
║                   F类：互动/传播类评分框架                                   ║
╚══════════════════════════════════════════════════════════════════════════════╝

【分析目标】
评估回复/引用/转发其他账号的内容，或社区传播现象的传播潜力。

═══════════════════════════════════════════════════════════════════════════════

📋 **第一步：判断互动类型**

**【什么是"单向提及"？】**
- 发起者单方面@/提及/挑战大IP，而大IP没有实际参与
- 示例："KOL发起挑战：如果CZ做XX，则捐款YY"、"@CZ 请来看看"
- 判断标准：大IP没有回复/引用/点赞/转发，只是被单向提及

**【什么是"真实互动"？】**
- 大IP实际参与了互动：回复了、引用了、转发了、或点赞了
- 示例：CZ回复了推文、Elon转发了推文、何一点赞了推文
- 判断标准：有明确证据表明大IP有实际回应动作

⚠️ **单向提及的权重规则**：
- **如果只是单向提及大IP**：不能按大IP的权重计算，必须按实际发起者的权重计算
- **发起者是普通KOL**：按普通KOL权重（12-18分），不能按CZ/Elon等大IP权重（25-30分）
- **理由**：单向提及只是借大IP知名度营销，大IP没有实际参与，事件价值取决于发起者

═══════════════════════════════════════════════════════════════════════════════

📋 **第二步：分量等级评估**（S-E级）

**S级分量**：与何一/CZ/Elon/Trump等世界级人物**真实互动**（大IP实际回应）→ 40分
**A级分量**：与知名机构/品牌、知名KOL**真实互动** → 32分
**B级分量**：与普通KOL/账号互动 → 24分
**C级分量**：与普通账号互动 → 12分
**D级分量**：单向提及大IP但发起者本身无影响力 → 5分
**E级分量**：完全无价值的互动 → 0分

**有意义性判断**：
- S/A级（真实互动）→ ✓✓有意义（与大IP真实互动有社交价值）
- B级 → 条件性有意义（需要有观点/内容）
- C级 → ✗通常无意义（普通账号互动价值有限）
- D级（单向提及）→ ✗无意义（营销噱头，大IP未参与）
- E级 → ✗无意义

⚠️ **C/D/E级分量直接阻断**

═══════════════════════════════════════════════════════════════════════════════

📋 **第三步：硬性阻断条件检查**

**1. 无观点的互动**：只是"赞"、"转了"、表情 → pass=false
**2. 纯营销转发**：KOL转发营销内容获取推广费 → pass=false
**3. 单向提及**：KOL发起挑战/@大IP但大IP未参与 → pass=false

⚠️ **豁免条件（发现型meme不阻断）**：
- **豁免**：如果推文是**发现型meme**（发现IP的隐藏元素/彩蛋/口号/设计细节等）→ 不触发"单向提及"阻断
  - 判断标准：
    1. 推文描述了具体的发现内容（隐藏图案、口号、彩蛋、设计细节等）
    2. 发现内容与被引用的IP直接相关（Yzi Labs的海报、CZ的推文背景等）
    3. 发现内容本身有传播价值（有趣的巧合、隐藏的含义、视觉元素等）
  - 示例：
    - ✅ "发现Yzi Labs海报的真正口号是WE MAKE IMPACT" → 不触发（发现型meme）
    - ✅ "发现CZ推文背景中的隐藏图案" → 不触发（发现型meme）
    - ❌ "@CZ 请来看看这个项目" → 触发（单向提及挑战，无实质内容）
  - 理由：发现型meme有实质内容和传播价值，不同于单纯的蹭热度/单向提及

═══════════════════════════════════════════════════════════════════════════════

📋 **第四步：综合评分**

总分 = 基础分 + 被互动方权重 + 时效性

**基础分数**：S级40分、A级32分、B级24分、C级12分、D级5分、E级0分

**被互动方权重**（0-30分）：
- 世界级IP真实互动（何一、CZ、Elon、Trump实际参与）：25-30分
- 知名KOL/机构真实互动：20-25分
- 普通KOL：12-18分
- 普通账号：5-10分
- **⚠️ 单向提及大IP**：只能按发起者权重（普通KOL或普通账号），不能按大IP权重

**时效性加分**（0-20分）：
- 近期互动（7天内）：+15分
- 中期互动（30天内）：+10分
- 远期互动（超过30天）：0分

**示例**：
- "CZ回复了推文" → S级(40) + 世界级IP真实互动(30) + 近期(15) = 85分
- "Binance回复了推文" → A级(32) + 知名机构真实互动(25) + 近期(15) = 72分
- "KOL发起挑战：如果CZ更换头像为代币PFP，则捐款$10000做慈善" → D级分量(5) + 普通KOL权重(15) + 近期(15) = 35分（无意义，不能按CZ权重计算）
- "普通账号@CZ请来看" → D级(5) + 普通账号(8) + 近期(15) = 28分（无意义）
- "回复普通账号推文" → C级(12) + 普通账号(8) + 近期(15) = 35分（通常无意义）

═══════════════════════════════════════════════════════════════════════════════

【输出格式】

**只返回JSON**：

**C/D/E级或阻断**：
{"pass": false, "blockReason": "原因", "magnitudeLevel": "C/D/E", "categoryAnalysis": null}

**通过**：
{
  "pass": true,
  "blockReason": null,
  "categoryAnalysis": {
    "category": "F",
    "categoryName": "互动/传播类",
    "interactionType": "真实互动/单向提及",
    "magnitudeLevel": "S/A/B",
    "magnitudeScore": 40,
    "weightScore": 30,
    "timelinessScore": 15,
    "totalScore": 85,
    "interactedParty": "被互动的IP名称"
  },
  "blockChecks": {"hardBlocks": [], "passedChecks": ["检查1", "检查2"]}
}

⚠️ totalScore ≥ 60 建议通过
`;
}
