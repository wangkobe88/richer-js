/**
 * Stage 2：E类（社会热点/现象类）评分 Prompt
 * V1.0 - 3阶段架构的第二阶段
 */

export const CATEGORY_E_PROMPT_VERSION = 'V1.0';

export function buildCategoryEPrompt(eventDescription, eventClassification) {
  return `你是E类（社会热点/现象类）事件评分专家。包括发现型meme和娱乐热点。

【事件描述】
主题：${eventDescription.eventTheme}
主体：${eventDescription.eventSubject}
是否超大IP：${eventDescription.isLargeIP}
事件内容：${eventDescription.eventContent}
时效性：${eventDescription.eventTiming}
关键实体：${eventDescription.keyEntities?.join(', ') || '无'}
关键数据：${JSON.stringify(eventDescription.keyData || {})}
性质标记：${eventClassification?.propertyMarkers ? JSON.stringify(eventClassification.propertyMarkers) : '无'}

╔══════════════════════════════════════════════════════════════════════════════╗
║                E类：社会热点/现象类评分框架                                  ║
╚══════════════════════════════════════════════════════════════════════════════╝

【分析目标】
评估引发全网讨论的事件、病毒传播的内容、文化现象、社会热点的传播潜力。
⚠️ 包括发现型meme（发现IP的隐藏元素、彩蛋、口号、设计细节等）

═══════════════════════════════════════════════════════════════════════════════

📋 **第一步：分量等级评估**（S-E级）

**【常规热点/现象类】**：
- S级分量：世界级热点（全球热搜、亿级传播）→ 40分
- A级分量：全国级热点（全国热搜、千万级传播）→ 32分
- B级分量：平台级热点（抖音/B站爆款、百万级传播）→ 24分
- C级分量：社区级讨论（有一定热度但非平台级）→ 12分
- D级分量：地区性/本地化事件（仅限于某个城市/地区）→ 5分
- E级分量：完全无热度的小范围讨论 → 0分

**【发现型meme类】**（基于发现内容的价值，而非发现者粉丝数）：
- S级分量：发现世界级IP的**重大隐藏信息**（品牌真正口号、核心概念）→ 40分
- A级分量：发现知名IP/项目的**有趣彩蛋/设计细节**（Yzi Labs海报口号、CZ推文背景图案）→ 32分
- B级分量：发现IP的**一般性隐藏元素**（logo设计巧思、网站彩蛋）→ 24分
- C级分量：发现较次要的细节（趣味性有限）→ 12分
- D/E级分量：发现内容无价值或过于牵强 → 0分

⚠️ **发现型meme的评分关键**：
- **不关注发现者的粉丝数**（14粉丝的小账号也可以发现高价值内容）
- **关注发现内容本身的价值**（口号、彩蛋、设计细节的有趣程度）
- **关注被发现的IP影响力**（Yzi Labs、CZ、Binance等知名IP）

⚠️ **D/E级分量直接阻断**

═══════════════════════════════════════════════════════════════════════════════

📋 **第二步：硬性阻断条件检查**

**1. 热度真实性阻断**：
   - 营销号制造的虚假热点（没有真实传播数据）→ pass=false
   - 单向宣称的热度（只有创作者自述，无第三方验证）→ pass=false
   - 平台运营包装成热点 → pass=false

**2. 传播价值阻断**：
   - 短暂猎奇话题（一时猎奇但无持续传播价值）→ pass=false
   - 缺乏meme元素（满足0项即触发）→ pass=false
     * 无可重复/衍生/变形的元素
     * 无用户自发二创（模仿、玩梗、恶搞）
     * 无法脱离原生语境独立传播
     * 无情感内核（愤怒/崇拜/同情/共鸣等）

**3. 内容性质阻断**：
   - 政治/地缘政治事件（满足1项即触发）→ pass=false
     * 豁免：有明显幽默/讽刺元素的政治meme
     * 豁免：大IP（如CZ、Musk）发表政治观点引发社区玩梗
   - 纯负面/愤怒事件（无正向情感）→ pass=false
     * 豁免：同情/保护事件（动物保护、弱势群体关怀）

**4. 娱乐热点阻断**：
   - 营销号八卦（无可信来源、可能编造）→ pass=false
   - 配合营销的"八卦"（明星配合新片/专辑发布的营销）→ pass=false
   - 无争议/无爆点的日常（明星吃喝拉撒）→ pass=false

═══════════════════════════════════════════════════════════════════════════════

📋 **第三步：综合评分**

总分 = 基础分 + 热度/IP权重 + 时效性

**基础分数**：S级40分、A级32分、B级24分、C级12分、D级5分、E级0分

**热度/IP权重**（0-30分）：

**【常规热点/现象类】**（基于传播数据）：
- 亿级传播/全球热搜：25-30分
- 千万级传播/全国热搜：20-25分
- 百万级传播/平台爆款：15-20分
- 十万级传播/社区热点：10-15分
- 万级以下：5-10分

**【发现型meme类】**（基于被发现的IP影响力）：
- 世界级IP（CZ、Binance、Tesla、Apple等）：25-30分
- 知名IP/项目（Yzi Labs等知名项目、知名KOL的IP）：20-25分
- 普通IP/项目：12-18分
- 小众IP：5-10分

⚠️ **关键区别**：
- 常规热点看**传播数据**（播放量、转发量）
- 发现型meme看**IP影响力**（被发现的IP本身有多知名）

**时效性加分**（0-20分）：
- 正在发酵（7天内）：+15分
- 持续热点（30天内）：+10分
- 过气热点（超过30天）：0分

**示例**：
- "抖音爆款视频千万播放" → B级(24) + 百万级(18) + 正在发酵(15) = 57分
- "发现Yzi Labs海报的真正口号是WE MAKE IMPACT" → A级(32) + Yzi Labs知名IP(20) + 正在发酵(15) = 67分
- "发现CZ推文背景中的隐藏图案" → A级(32) + CZ世界级IP(28) + 正在发酵(15) = 75分

═══════════════════════════════════════════════════════════════════════════════

【输出格式】

**只返回JSON**：

**D/E级或阻断**：
{"pass": false, "blockReason": "原因", "magnitudeLevel": "D/E", "categoryAnalysis": null}

**通过**：
{
  "pass": true,
  "blockReason": null,
  "categoryAnalysis": {
    "category": "E",
    "categoryName": "社会热点/现象类",
    "eventType": "常规热点/发现型meme",
    "magnitudeLevel": "S/A/B/C",
    "magnitudeScore": 32,
    "weightScore": 20,
    "timelinessScore": 15,
    "totalScore": 67,
    "discoveryType": "发现型meme"  // 如果是发现型
  },
  "blockChecks": {"hardBlocks": [], "passedChecks": ["检查1", "检查2"]}
}

⚠️ totalScore ≥ 60 建议通过
`;
}
