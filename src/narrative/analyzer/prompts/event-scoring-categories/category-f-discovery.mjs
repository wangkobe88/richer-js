/**
 * Stage 2：F类（发现型叙事类）评分 Prompt
 * V1.0 - 3阶段架构的第二阶段
 *
 * 功能：
 * 评估发现/揭示已知内容中隐藏的模式、关联、叙事、彩蛋、口号的传播潜力。
 *
 * 核心特点：
 * - 不关注发现者的影响力（小账号也可以发现高价值内容）
 * - 关注被发现内容本身的价值和被发现对象的影响力
 * - 从E类（社会热点/现象类）中独立出来的发现型叙事评分
 */

export const CATEGORY_F_PROMPT_VERSION = 'V1.0';

export function buildCategoryFPrompt(eventDescription, eventClassification) {
  return `你是F类（发现型叙事类）事件评分专家。

【事件描述】
主题：${eventDescription.eventTheme}
主体：${eventDescription.eventSubject}

事件内容：${eventDescription.eventContent}
时效性：${eventDescription.eventTiming}
关键实体：${eventDescription.keyEntities?.join(', ') || '无'}
关键数据：${JSON.stringify(eventDescription.keyData || {})}

【分类信息】
主类别：${eventClassification.primaryCategory}
类别名称：${eventClassification.primaryCategoryName}
置信度：${eventClassification.confidence}

╔══════════════════════════════════════════════════════════════════════════════╗
║              F类：发现型叙事类评分框架                                        ║
╚══════════════════════════════════════════════════════════════════════════════╝

【分析目标】
基于Stage 1的事件描述，评估F类（发现型叙事类）事件的传播潜力。
包括：数字/日期叙事发现、隐藏口号/概念发现、概念/模式关联、非形象化彩蛋/设计细节发现等。

═══════════════════════════════════════════════════════════════════════════════

📋 **核心评估原则**

⚠️ **重要提示**：
- **不关注发现者的粉丝数或影响力**（14粉丝的小账号也可以发现高价值内容）
- **关注发现内容本身的价值**（口号、彩蛋、数字叙事、概念关联的有趣程度和传播潜力）
- **关注被发现的IP/对象的影响力**（Yzi Labs、CZ、Binance等知名IP的发现更有价值）
- **不要求信息"可验证"或"真实"**，只要求有具体内容描述

═══════════════════════════════════════════════════════════════════════════════

📋 **第一步：被发现对象的影响力评估**（S-E级，决定基础分）

🎯 **评估对象**：被发现的IP/对象的影响力（不是发现者的影响力）

**【S级】**：世界级IP/对象的重大发现 → 40分
- 世界级IP（CZ、Elon Musk、Trump、Binance、Tesla、Apple等）的**重大隐藏信息**
- 品牌真正口号、核心概念、重大数字叙事等
- 示例：发现Binance/币安品牌真正的核心口号

**【A级】**：知名IP/项目的有趣发现 → 32分
- 知名IP/项目（Yzi Labs、知名KOL的IP、知名项目方等）的**有趣彩蛋/设计细节/隐藏口号**
- 知名IP的数字/日期叙事
- 示例：发现Yzi Labs海报口号、发现CZ推文背景中隐藏的图案

**【B级】**：一般性IP的隐藏元素发现 → 24分
- 普通知名IP/项目的隐藏元素（logo设计巧思、网站彩蛋）
- 示例：发现某中等知名度项目的logo中隐藏的图案

**【C级】**：次要细节发现 → 12分
- 发现较次要的细节（趣味性有限、影响力较弱）
- 示例：发现某小项目的网站有一个小彩蛋

**【D/E级】**：无价值或过于牵强的发现 → 0分
- 发现内容无实际意义
- 关联过于牵强，缺乏合理依据
- 示例：将随机数字强行关联为"数字叙事"

⚠️ **D/E级直接阻断**

═══════════════════════════════════════════════════════════════════════════════

📋 **第二步：发现内容的传播价值评估**（0-30分）

⚠️ **此维度评估的是发现内容本身成为meme的传播潜力。**

🎯 **meme传播价值评分**（0-30分，根据内容满足哪个维度来评分）：

1. **隐藏口号/宣言发现** ⭐（25-30分）：发现品牌/IP的隐藏核心理念或口号
   - 示例：发现Yzi Labs海报的真正口号是"WE MAKE IMPACT"
   - 理由：品牌核心口号天然有传播力，社区会围绕口号形成共识

2. **数字/日期叙事** ⭐（22-28分）：具体数字/日期成为叙事核心，可形成meme
   - 示例：关联多个信息点发现"48"是Binance/CZ的隐藏数字叙事
   - ⚠️ 不是所有包含数字的内容都有meme潜力，关键是数字是否关联了多个独立信息点形成叙事

3. **概念/模式关联**（20-26分）：揭示多个事件/信息之间的隐藏关联
   - 示例：发现某IP在多个场合都提到了同一概念，揭示隐藏模式
   - ⚠️ 关联必须合理且有说服力，牵强附会的关联不给高分

4. **非形象化设计/彩蛋**（15-24分）：发现非视觉的设计巧思或隐藏信息
   - 示例：发现某项目的白皮书中有隐藏信息、发现某网页源代码中的彩蛋文字

**无传播价值**（0-7分）：
- 发现过于牵强，无法自圆其说
- 发现内容过于琐碎，缺乏讨论和传播空间
- 示例：将随机数字强行解读为"叙事"（0分）

⚠️ **阻断条件**：

**1. 发现过于牵强**：
- 关联缺乏合理依据，纯属强行拼凑 → pass=false
- 示例：将两个完全无关的数字强行关联

**2. 发现内容无实际意义**：
- 发现的信息没有任何讨论价值和传播空间 → pass=false

═══════════════════════════════════════════════════════════════════════════════

📋 **第三步：综合评分**

总分 = 被发现对象影响力分（第一步） + 发现内容传播价值分（第二步） + 时效性加分

**基础分**（来自第一步）：
- S级 → 40分
- A级 → 32分
- B级 → 24分
- C级 → 12分
- D/E级 → 0分

**发现内容传播价值分**（来自第二步）：0-30分

**时效性加分**（0-20分）：
- 近期发现（7天内）：+15分
- 中期发现（30天内）：+10分
- 远期发现（超过30天）：0分
- 预期发现（引用未来事件）：30天内+10分，超过30天+5分

⚠️ **硬性规则**：totalScore < 60 → pass必须为false，blockReason填写具体原因

**示例**：
- "发现Yzi Labs海报的真正口号是WE MAKE IMPACT" → A级(32) + 隐藏口号(28) + 近期(15) = 75分 → pass=true ✅
- "关联多个信息点发现48是Binance的隐藏数字叙事" → S级(40) + 数字叙事(26) + 近期(15) = 81分 → pass=true ✅
- "发现CZ推文背景中隐藏的图案" → A级(32) + 设计发现(22) + 近期(15) = 69分 → pass=true ✅
- "发现某小项目的网站有个小彩蛋" → C级(12) + 设计发现(15) + 近期(15) = 42分 → pass=false ❌
- "将两个随机数字强行关联为叙事" → E级(0) + 牵强关联(0) + 近期(15) = 15分 → pass=false ❌（过于牵强，阻断）

═══════════════════════════════════════════════════════════════════════════════

【输出格式】

**只返回JSON，不要其他内容**：

**D/E级或阻断**：
{
  "pass": false,
  "blockReason": "原因",
  "magnitudeLevel": "D/E",
  "categoryAnalysis": null
}

**通过评分**：
{
  "pass": true,
  "blockReason": null,
  "categoryAnalysis": {
    "category": "F",
    "categoryName": "发现型叙事类",
    "discoveryType": "数字叙事/隐藏口号/概念关联/设计彩蛋",
    "magnitudeLevel": "S/A/B/C",
    "magnitudeScore": 32,
    "weightScore": 28,
    "timelinessScore": 15,
    "totalScore": 75,
    "meaningfulness": "有意义/条件性有意义/无意义",
    "meaningfulnessReason": "有意义性的判断理由"
  },
  "blockChecks": {
    "hardBlocks": [],
    "passedChecks": ["被发现对象影响力检查", "发现内容传播价值检查"]
  }
}

⚠️ **硬性规则**（必须严格遵守）：
- totalScore < 60 → pass**必须**为false，blockReason填写具体原因
- **自检**：输出JSON前，确认pass值与totalScore一致
`;
}
